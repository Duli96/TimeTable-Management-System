    private Connection DoConnect(){
        Connection conn=null;
        try{
            Class.forName("com.mysql.jdbc.Driver");   
            conn = DriverManager.getConnection(DB_URL, username, password);
        }catch (ClassNotFoundException ex) {
            Logger.getLogger(RoomForLecturer.class.getName()).log(Level.SEVERE, null, ex);
        }catch (SQLException ex) {
            Logger.getLogger(RoomForLecturer.class.getName()).log(Level.SEVERE, null, ex);
        }          
        return conn;
    }
    
    
    private void currentLecturers(){
        
        Connection conn = DoConnect();
        
        try{
            ResultSet rs = conn.createStatement().executeQuery("SELECT * FROM lecturer"); 
            while(rs.next()){
                sList2.add(rs.getString(1));
                selectLecturerCombo.addItem(String.format("%s | %s",rs.getString(3),rs.getString(2)));
            }
        }catch (SQLException ex){
            Logger.getLogger(RoomForLecturer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void currentRooms(){
    
        Connection conn = DoConnect();    
        try {            
            ResultSet rs = conn.createStatement().executeQuery("SELECT * FROM rooms");              
            while(rs.next()){     
                sList.add(rs.getString(1));
                selectRoomCombo.addItem(String.format("%s | (%s)",rs.getString(2),rs.getString(3)));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(RoomForLecturer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
