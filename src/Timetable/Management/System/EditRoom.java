    // database connection
    private Connection DoConnect(){
        Connection conn=null;
        try{
            Class.forName("com.mysql.jdbc.Driver");   
            conn = DriverManager.getConnection(DB_URL, username, password);
        }catch (ClassNotFoundException ex) {
            Logger.getLogger(DeleteBuilding.class.getName()).log(Level.SEVERE, null, ex);
        }catch (SQLException ex) {
            Logger.getLogger(DeleteBuilding.class.getName()).log(Level.SEVERE, null, ex);
        }          
        return conn;
    }
    
    // show registered buildings in combobox
    private void currentRooms(){
        Connection conn = DoConnect();    
        try {            
            ResultSet rs = conn.createStatement().executeQuery("SELECT * FROM rooms");
            ResultSet rs2 = conn.createStatement().executeQuery("SELECT * FROM buildings");
            
            while(rs.next()){     
                sList.add(rs.getString(1));
                jComboBox1.addItem(String.format("%s | (%s)  ",rs.getString(2),rs.getString(3)));
                roomcode.setText(rs.getString(2));
                roomname.setText(rs.getString(3));
                setRoomTag.addItem(String.format("%s",rs.getString(4)));
                //setBuilding.addItem(String.format("%s",rs2.getString(1)));
            }
            while(rs2.next()){     
                    sLis2.add(rs2.getString(1));
                    setBuilding.addItem(String.format("%s | (%s)",rs2.getString(2),rs2.getString(3)));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(EditBuilding.class.getName()).log(Level.SEVERE, null, ex);
        }              
    }
    
    
