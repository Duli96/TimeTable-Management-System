    private void EditRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditRoomActionPerformed
        // TODO add your handling code here:
        Connection conn = DoConnect();    
        PreparedStatement editStmt;
        try {
            if (JOptionPane.showConfirmDialog(this,"This Will Update Room Details! Proceed?")==0){
                editStmt = conn.prepareStatement("UPDATE rooms SET room_code = ?, room_name = ?, room_tag = ?, room_building = ? WHERE room_id = ?;");                     
                editStmt.setString(1,roomcode.getText());
                editStmt.setString(2,roomname.getText());
                editStmt.setString(3,setRoomTag.getSelectedItem().toString());
                editStmt.setString(4,setBuilding.getSelectedItem().toString());
                editStmt.setInt(5,Integer.parseInt((String)sList.get(jComboBox1.getSelectedIndex())));   
                editStmt.executeUpdate();
                //editStmt.execute();
                JOptionPane.showMessageDialog(this, "Record Updated");                
            }else{
                JOptionPane.showMessageDialog(this, "Aborted!");                
            }
            
        }catch (SQLException ex) {
            Logger.getLogger(DeleteBuilding.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_EditRoomActionPerformed

