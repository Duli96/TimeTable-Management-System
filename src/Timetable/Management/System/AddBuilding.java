    private void AddBuildingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBuildingActionPerformed
        // TODO add your handling code here:
        String bId = buildingid.getText();
        String bName = buildingname.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection(DB_URL, username, password);
            insert = conn.prepareStatement("INSERT INTO buildings(building_code,building_name)VALUES(?,?)");
            
            // insert data to relavant ? marks
            insert.setString(1, bId);
            insert.setString(2, bName);
            
            // execute the query
            insert.executeUpdate();
            
            // set pop-up message
            JOptionPane.showMessageDialog(this, "Record Added");
            
            // clear text boxes
            buildingid.setText("");
            buildingname.setText("");
            
            // focus on first textbox after clearing
            buildingid.requestFocus();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddBuilding.class.getName()).log(Level.SEVERE, null, ex);
            
        } catch (SQLException ex) {
            Logger.getLogger(AddBuilding.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_AddBuildingActionPerformed

