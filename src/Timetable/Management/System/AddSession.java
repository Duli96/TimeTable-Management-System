    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        Connection con;
        PreparedStatement insert;
        
        String subName = (String)subject.getSelectedItem( );
        String code = subcode.getText();
        String lecturersList = lecturers.getSelectedValuesList().toString().replace("[", " ");
        String tag = (String)tags.getSelectedItem();
        String grpId = (String)grpid.getSelectedItem();
        String stdQty = studentcnt.getText();
        String durations = duration.getText();
        
        //new

        try {
            
            //Insert data to db           
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/timetablems","root","1234");
            
            insert = con.prepareStatement("insert into session(lecturers,tags,groups,subject,subcode,hours,stdcount) values(?,?,?,?,?,?,?)");
            insert.setString(1, lecturersList.toString().replace("]", " "));
            insert.setString(2, tag);
            insert.setString(3, grpId);
            insert.setString(4, subName);
            insert.setString(5, code);
            insert.setString(6, durations);
            insert.setString(7, stdQty);
            
            insert.executeUpdate();
            
            //preview session
            String labelText = lecturersList.toString().replace("]", " ");
            
            lecLabel.setText(labelText);
            subcodeLabel.setText(subName+" ("+code+")");
            tagLabel.setText(tag);
            groupLabel.setText(grpId);
            stdHourLabel.setText(stdQty+" ("+ durations + ")");  
            
            
            JOptionPane.showMessageDialog(this, "Session Created!");
           
            
            studentcnt.setText("");
            duration.setText("");
            subcode.setText("");            
            
           
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddSubject.class.getName()).log(Level.SEVERE, null, ex);
        }catch (SQLException ex) {
            Logger.getLogger(AddSubject.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_submitActionPerformed

