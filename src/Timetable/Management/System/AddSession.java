    //get group
    private void getGrpList(){
        try { 
             Class.forName("com.mysql.jdbc.Driver");

             Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/timetablems","root","1234");
             
             String grpSelect= (String)tags.getSelectedItem();
             
             if("Lab".equals(grpSelect)){
                
                 tagLabelForm.setText("Sub Group");
                Statement statement = connection.createStatement();
                String query = "SELECT * FROM subgroupids ";
                ResultSet rs = statement.executeQuery(query);

                while (rs.next())
                {                             
                   grpid.addItem(rs.getString("subgroupid"));
                }//end while
                
             }else{
                tagLabelForm.setText("Group ID");
                Statement statement = connection.createStatement();
                String query = "SELECT * FROM groupids ";
                ResultSet rs = statement.executeQuery(query);

                while (rs.next())
                {                             
                   grpid.addItem(rs.getString("groupid"));
                }//end while
             }
             connection.close();
             } catch (Exception e) {
                  e.printStackTrace();
             }
    }
    
    
