    private void addYearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addYearActionPerformed
        try {
            createDBTable();
        } catch (SQLException ex) {
            Logger.getLogger(AddProgram.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddProgram.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String input = program.getText();
        String uniqueID = UUID.randomUUID().toString();
        if(input.equals("")){
        
            JOptionPane.showMessageDialog(this, "Invalid Input","Error",JOptionPane.ERROR_MESSAGE);
        
        }else{
            try {
                Statement stmt = new DBconnection().getDB().createStatement();
                String sql = "INSERT INTO Program "
                                +" VALUES ("
                                +"'"+uniqueID+"' ,"
                                +"'"+input+"')";
             
             stmt.executeUpdate(sql);
                
                
                
                
                JOptionPane.showMessageDialog(this,  "Program "+input+" has added succesfully","Succesful",JOptionPane.INFORMATION_MESSAGE);
            program.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(AddProgram.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Invalid Input","Error",JOptionPane.ERROR_MESSAGE);
                program.setText("");
            }
            
            
        
        }
    }//GEN-LAST:event_addYearActionPerformed

