    private void addYearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addYearActionPerformed
        
        try {
            createDBTable();
        } catch (SQLException ex) {
            Logger.getLogger(AddSubGroup.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddSubGroup.class.getName()).log(Level.SEVERE, null, ex);
        }

//get inputs
        String input = subgroup.getText();
        
        try{
        if(input.equals("")){
        
        JOptionPane.showMessageDialog(this, "Invalid Input","Error",JOptionPane.ERROR_MESSAGE);
        
        }else {
        
           int number = Integer.parseInt(input);
           String uniqueID = UUID.randomUUID().toString();
           
           
           //con = new DBconnection().getDB();
           
         Statement stmt = con.createStatement();
           
             String sql = "INSERT INTO SubGroupNumbers "
                                +" VALUES ("
                                +"'"+uniqueID+"' ,"
                                +number+")";
             
             stmt.executeUpdate(sql);
             
        //succes msg
        JOptionPane.showMessageDialog(this, "Sub-group number "+ number+" has added succesfully","Succesful",JOptionPane.INFORMATION_MESSAGE);
        subgroup.setText("");
        }
        }catch(Exception e){
         subgroup.setText("");
        JOptionPane.showMessageDialog(this, "Invalid Input","Error",JOptionPane.ERROR_MESSAGE);
        
        }
    }//GEN-LAST:event_addYearActionPerformed

